
public class IfDemo {

	public static void main(String[] args) {
		int age=50;
		
		//IF Statement
		if(age>=18) {
			System.out.println("Eligible for voting");
		}
		
		//IF Else Statement
		
        if (age>=18) {
        	System.out.println("Eligible for voting");
        }
        else {
        	System.out.println("Not Eligible for voting");
        }
	}
	
	

}
